import User from '../models/User.js';
import jwt from 'jsonwebtoken';
import config from '../config/constants.js';

// Normalize jwt expire value (accepts '7d', '7 days', number seconds, etc.)
const normalizeExpire = (value) => {
  if (!value) return '7d';
  const v = String(value).trim();

  // If purely numeric, treat as seconds
  if (/^\d+$/.test(v)) return Number(v);

  // Common human forms -> short form accepted by jsonwebtoken
  let normalized = v
    .toLowerCase()
    .replace(/\s+/g, '')
    .replace(/days?/, 'd')
    .replace(/hours?/, 'h')
    .replace(/minutes?/, 'm')
    .replace(/seconds?/, 's');

  // If still malformed, fallback to 7d
  if (!/^[0-9]+[dhms]?$/.test(normalized) && !/^[0-9]+$/.test(normalized)) {
    return '7d';
  }

  return normalized;
};

// Generate JWT Token
const generateToken = (id) => {
  const expiresIn = normalizeExpire(config.JWT_EXPIRE);
  return jwt.sign({ id }, process.env.JWT_SECRET, {
    expiresIn,
  });
};

// @desc    Register user
// @route   POST /api/auth/signup
// @access  Public
export const signup = async (req, res) => {
  try {
    const { name, email, password, confirmPassword } = req.body;

    // Validation
    if (!name || !email || !password || !confirmPassword) {
      return res.status(400).json({ message: 'All fields are required' });
    }

    if (password !== confirmPassword) {
      return res.status(400).json({ message: 'Passwords do not match' });
    }

    // Check if user already exists
    const existingUser = await User.findOne({ email });
    if (existingUser) {
      return res.status(409).json({ message: 'User already exists' });
    }

    // Create new user
    const user = await User.create({
      name,
      email,
      password,
    });

    const token = generateToken(user._id);

    res.status(201).json({
      success: true,
      message: 'User registered successfully',
      token,
      user: {
        id: user._id,
        name: user.name,
        email: user.email,
        role: user.role,
      },
    });
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

// @desc    Login user
// @route   POST /api/auth/login
// @access  Public
export const login = async (req, res) => {
  try {
    const { email, password } = req.body;

    // Validation
    if (!email || !password) {
      return res.status(400).json({ message: 'Email and password are required' });
    }

    const user = await User.findOne({ email }).select('+password');

    if (!user) {
      return res.status(401).json({ message: 'Invalid credentials' });
    }

    const isMatch = await user.comparePassword(password);

    if (!isMatch) {
      return res.status(401).json({ message: 'Invalid credentials' });
    }

    const token = generateToken(user._id);

    res.status(200).json({
      success: true,
      message: 'User logged in successfully',
      token,
      user: {
        id: user._id,
        name: user.name,
        email: user.email,
        role: user.role,
      },
    });
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

// @desc    Get user profile
// @route   GET /api/auth/profile
// @access  Private
export const getProfile = async (req, res) => {
  try {
    const user = await User.findById(req.userId);

    if (!user) {
      return res.status(404).json({ message: 'User not found' });
    }

    res.status(200).json({
      success: true,
      user: {
        id: user._id,
        name: user.name,
        email: user.email,
        role: user.role,
        avatar: user.avatar,
      },
    });
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};
